#include <stdio.h>

int main() {
  int a, b, c;
  
  a = 81;
  printf("Nilai awal:\n");
  printf("a = %d\n", a);
  
  /* mengubah nilai */
  a = b = c = 72;
  
  printf("\nNilai akhir:\n");
  printf("a = %d\n", a);
  printf("b = %d\n", b);
  printf("c = %d\n", c); 
  
  return 0;
}

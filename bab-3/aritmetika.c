#include <stdio.h>

int main() {
  int a=18, b=6;
  
  printf("a + b = %d\n", (a+b));
  printf("a - b = %d\n", (a-b));
  printf("a * b = %d\n", (a*b));
  printf("a / b = %d\n", (a/b));
  printf("a %% b = %d\n", (a%b));
  printf("++a   = %d\n", ++a);
  printf("--b   = %d\n", --b);
    
  return 0;
}

#include <iostream>
#include <cmath>
using namespace std;
int main(){
	cout<<"sqrt	:"<<sqrt(64)<<endl; //akar
	cout<<"round	:"<<round(2.6)<<endl;
	cout<<"log	:"<<log(30)<<endl;
	cout<<"sin	:"<<sin(30)<<endl; //sin
	cout<<"abs	:"<<abs(50)<<endl;//return the absolute value of x(mengembalikan nilai absolut x)
	cout<<"acos	:"<<acos(-1)<<endl;//returns the arccosine of x, in radians
	cout<<"asin	:"<<asin(-1)<<endl;
	cout<<"atan	:"<<atan(30)<<endl;
	cout<<"cbrt	:"<<cbrt(30)<<endl;
	cout<<"ceil	:"<<ceil(30)<<endl;
	cout<<"cos	:"<<cos(30)<<endl;
	cout<<"cosh	:"<<cosh(30)<<endl;
	cout<<"exp	:"<<exp(30)<<endl;
	cout<<"exmp1	:"<<expm1(30)<<endl;
	cout<<"fabs	:"<<fabs(30)<<endl;
	cout<<"fdim	:"<<fdim(30,2)<<endl;
	cout<<"floor	:"<<floor(30)<<endl;
	cout<<"hypot	:"<<hypot(30,20)<<endl;
	cout<<"fma	:"<<fma(30,5,5)<<endl;
	cout<<"fmax	:"<<fmax(5,10)<<endl;
	cout<<"fmin	:"<<fmin(5,10)<<endl;
	cout<<"fmod	:"<<fmod(10,5)<<endl;
	cout<<"pow	:"<<pow(10,2)<<endl;
	cout<<"sinh	:"<<sinh(30)<<endl;
	cout<<"tan	:"<<tan(30)<<endl;
	cout<<"tanh	:"<<tanh(30)<<endl;
	return 0;
}
